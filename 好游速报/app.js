    bookMark(postId) {
        const { bookMarkList,userInfo,loginStatus } = this.globalData;
        if (!userInfo || !loginStatus) {
            wx.showToast({
              title: '请先登录',
              icon: 'none',
              duration: 2000,
            });
            return;
        }
        postId = Number(postId);
        if (bookMarkList.includes(postId)) {
            wx.showToast({
              title: '请勿重复收藏',
              icon: 'none',
            });
            return;
          }
          bookMarkList.push(postId);
          wx.showToast({
          title: '收藏成功',
          icon: 'success',
    });
  },
  addComment(postId,commentContent) {
    const {comments,commentNum,userInfo,loginStatus,userComments} = this.globalData;
    if (!userInfo || !loginStatus) {
        wx.showToast({
          title: '请先登录',
          icon: 'none',
        });
        return false;
      }
    if (!commentContent.trim()) {
      wx.showToast({
        title: '评论内容不能为空',
        icon: 'none',
      });
      return false;
    }
    if (!comments[postId]) {
        comments[postId] = [];
        commentNum[postId] = 0;
      }
      const newComment = {
        user: userInfo.nickName,
        content: commentContent,
        time: new Date().toLocaleString(),
        postId: postId,
      };
      comments[postId].push(newComment);
      commentNum[postId] = comments[postId].length;
      userComments.push(newComment);
      wx.showToast({
        title: '评论成功',
        icon: 'success',
    });
    return true;
  },
  getComments(postId) {
    return this.globalData.comments[postId] || [];
  },
  getCommentNum(postId) {
    return this.globalData.commentNum[postId] || 0;
  },
  getUserComments() {
    return this.globalData.userComments;
  },
